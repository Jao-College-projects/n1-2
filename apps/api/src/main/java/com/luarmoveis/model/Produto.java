package com.luarmoveis.model;

import java.math.BigDecimal;

public class Produto {
    private long id;
    private String nome;
    private String categoria;
    private String descricaoCurta;
    private String descricaoLonga;
    private BigDecimal preco;
    private int estoque;
    private String imagem;
    private boolean destaqueCarrossel;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public String getDescricaoCurta() {
        return descricaoCurta;
    }

    public void setDescricaoCurta(String descricaoCurta) {
        this.descricaoCurta = descricaoCurta;
    }

    public String getDescricaoLonga() {
        return descricaoLonga;
    }

    public void setDescricaoLonga(String descricaoLonga) {
        this.descricaoLonga = descricaoLonga;
    }

    public BigDecimal getPreco() {
        return preco;
    }

    public void setPreco(BigDecimal preco) {
        this.preco = preco;
    }

    public int getEstoque() {
        return estoque;
    }

    public void setEstoque(int estoque) {
        this.estoque = estoque;
    }

    public String getImagem() {
        return imagem;
    }

    public void setImagem(String imagem) {
        this.imagem = imagem;
    }

    public boolean isDestaqueCarrossel() {
        return destaqueCarrossel;
    }

    public void setDestaqueCarrossel(boolean destaqueCarrossel) {
        this.destaqueCarrossel = destaqueCarrossel;
    }
}
